const killNumWay3 = (beilv, add) => {
  let killErr = 0;
  for (let i = 0; i < dataSample.length - 1; i++) {
    const sample = dataSample[i];
    const nextSample = dataSample[i + 1];
    const needKill = ((+sample[3] + +sample[4]) * beilv + add) % 10;
    if (nextSample?.slice(0, 3)?.includes(needKill)) {
      killErr += 1;
    }
  }
  console.log("误杀率:", killErr / dataSample.length, beilv, add);
  return killErr / dataSample.length;
};

